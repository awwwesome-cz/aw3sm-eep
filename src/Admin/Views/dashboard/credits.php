					<h2>Důvod pluginu</h2>
				</div>
				<div class="inside">
					<p>
						Plugin EEP se zaměřuje na rozšíření pluginů Elementor & Elementor Pro. Využívá stejné syntaxe a
						struktury kódu aby se co nejvíce připodobnil originálním pluginům. Každý widget je ručně
						vytvořen, aby přesně splňoval veškeré vlastnosti Elementor widgetů. Každý plugin využívá
						dědičnost tak aby při aktualizaci Elementoru bylo minimalizováno riziko nefunkčnosti pluginu
						nebo některé z jeho částí.
					</p>
				</div>
			</div>
